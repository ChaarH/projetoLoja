    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja atualizar o registro com as infomações inseridas?","Atualizar", JOptionPane.YES_NO_OPTION);
        if(resposta == JOptionPane.YES_OPTION){
            int id = Integer.parseInt(UsuarioId.getText());
            String nome = Nome.getText();
            String login = Login.getText();
            String senha = Senha.getText();
            
            FuncionariosModel objModel = new FuncionariosModel();

            objModel.setIdFun(id);
            objModel.setNomeFuncionario(nome);
            objModel.setLogin(login);
            objModel.setSenha(senha);

            FuncionariosDao objDao = new FuncionariosDao();
            try {
                objDao.atualizar(objModel);               
            } catch (SQLException ex) {
                Logger.getLogger(EditarUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

