    public void setCCliente(int i) throws SQLException {
        
        jTextCodCliente.setEnabled(false);
        jTextCPF.setEnabled(false);
        jTextRG.setEnabled(false);
        
        Functions fn = new Functions();
        ContatoModel objModel = new ContatoModel();
        
        ClienteDao objDao = new ClienteDao();
        ResultSet rs = objDao.listar(i);
        System.out.println(id);
        
        while(rs.next()) {
            jTextCodCliente.setText(String.valueOf((rs.getInt("ID_CLIENTE"))));
            jTextNome.setText(fn.ucFirst(rs.getString("TXT_NOME")));
            jTextEndereco.setText(fn.ucFirst(rs.getString("TXT_ENDERECO")));
            jTextTelefone.setText(fn.ucFirst(rs.getString("TXT_TELEFONE")));
            jTextEmail.setText(fn.ucFirst(rs.getString("TXT_EMAIL")));
            jTextRG.setText(rs.getString("TXT_RG"));
            jTextCPF.setText(fn.ucFirst(rs.getString("TXT_CPF")));
        }
    }
