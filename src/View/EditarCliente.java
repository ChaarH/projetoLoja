    private void jBtnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAtualizarActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Atualizar o registro com as novas informações inseridas?", "Atualizar", JOptionPane.YES_NO_CANCEL_OPTION);
        if(resposta == JOptionPane.YES_OPTION){
            int id = Integer.parseInt(jTextCodCliente.getText());
            String nome = jTextNome.getText();
            String email = jTextEmail.getText();
            String endereco = jTextEndereco.getText();
            String telefone = jTextTelefone.getText();

            ContatoModel objModel = new ContatoModel();

            objModel.setId(id);
            objModel.setNome(nome);
            objModel.setEmail(email);
            objModel.setEndereco(endereco);
            objModel.setTelefone(telefone);

            ClienteDao objDao = new ClienteDao();
            try {
                objDao.atualizar(objModel);
            } catch (SQLException ex) {
                Logger.getLogger(EditarCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBtnAtualizarActionPerformed

